package com.IPNC.main.VO;

public class HistoryVO {
		
		private String histTitle;
		private String histContent;
		private String histDate;
		private String imgPath;
		
		public String getHistTitle() {
			return histTitle;
		}
		public void setHistTitle(String histTitle) {
			this.histTitle = histTitle;
		}
		public String getHistContent() {
			return histContent;
		}
		public void setHistContent(String histContent) {
			this.histContent = histContent;
		}
		public String getHistDate() {
			return histDate;
		}	
		public void setHistDate(String histDate) {
			this.histDate = histDate;
		}
		public String getImgPath() {
			return imgPath;
		}
		public void setImgPath(String imgPath) {
			this.imgPath = imgPath;
		}
		
}
